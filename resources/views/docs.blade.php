			<h2 class="docTitle">Présentation</h2>
			<p>Aujourd'hui, toutes les crypto-monnaies sont des monnaies alternatives, car elles n'ont de cours légal dans aucun pays. Les crypto-monnaies utilisent un système de validation par preuve de travail pour les protéger des contrefaçons électroniques. De nombreuses crypto-monnaies ont été développées mais la plupart sont similaires et dérivent de la première implémentation complète : le Bitcoin </p>
			<p>Sauf exceptions, la majorité des crypto-monnaies est conçue pour que la création de nouvelles unités de monnaie soit graduelle, tout en plaçant, pour la plupart d'entre elles, un plafond à la masse monétaire qui sera à terme en circulation. Ceci est fait dans le but d'imiter la rareté (et la valeur) des métaux précieux et d'éviter l'hyperinflation. Comparées aux monnaies ordinaires maintenues par des institutions financières ou gardées en espèce, les crypto-monnaies sont gérées par un grand livre de comptes ouvert et consultable de tous (la blockchain) qui répertorie l'ensemble des transactions depuis l'origine. Une crypto-monnaie n'est donc pas occulte dans la mesure où n'importe quelle transaction est consultable sur internet avec mention de l'adresse électronique des comptes émetteur et receveur ainsi que du montant de la transaction. Pour autant, ces transactions sont infalsifiables et inviolables grâce au recours intensif à la cryptographie. Notons qu'il existe des exceptions à la règle de l'anonymat, comme le Darkcoin, le Zerocoin, le Bytecoin et Black Coin </p>
