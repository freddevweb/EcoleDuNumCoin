			<h2 class="docTitle">Histoire des crypto-monnaies</h2>
			<p>En 1998, Wei Dai a publié une description de « b-money », un système électronique de trésorerie anonyme. Peu après, Nick Szabo a créé le « Bit Gold » qui demandait aux utilisateurs de compléter une fonction de preuve de travail dont les solutions étaient chiffrées, mises ensembles et publiées. Le Bitcoin, créé en 2009 par un développeur (ou un groupe de développeurs) de logiciel utilisant le pseudonyme de Satoshi Nakamoto, utilise l'algorithme SHA-256 comme système de preuve de travail.  Plus tard, d'autres crypto-monnaies majeures ont été développées, comme le Litecoin (qui utilise scrypt comme preuve de travail et a des confirmations de transaction plus rapide), le Peercoin (qui utilise un système hybride de preuve de travail et a une inflation de 1 %) et le Namecoin (qui sert de DNS décentralisé, qui rend la censure d'internet plus difficile) ont été créées. Plusieurs autres crypto-monnaies ont été créées, toutes n'ont pas connu le succès, notamment celles apportant peu d'innovation. Durant les premières années d'existence, les crypto-monnaies ont gagné peu à peu l'attention des médias et du public. Depuis 2011, l'intérêt a rapidement augmenté, notamment durant la rapide montée du cours du Bitcoin en avril 2013. À partir de 2014, une seconde génération de crypto-monnaies est apparue, comme Monero, Ethereum et Nxt avec de nouvelles fonctionnalités telles que des adresses de furtivité, des contrats intelligents, le recours à des chaînes de bloc latérales ou adossées à des actifs tels que de l'or. </p>
			<p>Aujourd'hui les représentants de plusieurs banques centrales ont déclaré que l'adoption de crypto-monnaies telles que Bitcoin posent un défi important pour la capacité des banques centrales à influencer le prix du crédit pour toute l'économie. Ils ont également estimé que la popularité croissante des crypto-monnaies commerciales risque de faire perdre la confiance des consommateurs dans les monnaies fiduciaires. Gareth Murphy, un représentant de la banque centrale américaine a enfin déclaré que « l'utilisation généralisée [des crypto-monnaies] rendrait plus difficile pour les organismes statistiques de recueillir des données sur l'activité économique, elles-mêmes utilisées par les gouvernements pour orienter l'économie ». Il avertit que les crypto-monnaies constituent un nouveau défi pour le contrôle des fonctions importantes de la politique monétaire et de change des banques centrales.</p>
			<p>Un entrepreneur, fondateur de Robocoin, a lancé le premier distributeur automatique de bitcoin aux États-Unis le 20 février 2014. Le kiosque, installé à Austin dans le Texas, est similaire aux guichets automatiques bancaires, mais dispose de scanners pour lire les cartes d'identité afin de confirmer l'identité des utilisateurs.</p>
			<p>En janvier 2018, la banque d’Angleterre a annoncé souhaiter créer une crypto-monnaie indexée sur la monnaie britannique</p>
			<p>Au Canada et à Singapour, des institutions envisagent également de développer des systèmes de paiement officiels en crypto-monnaie</p>
